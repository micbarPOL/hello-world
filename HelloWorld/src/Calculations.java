public class Calculations {
    public static double add(double a, double b) {
        double c = a + b;
        return c;
    }

    public static double multiplying(double a, double b) {
        double c = a * b;
        return c;
    }

    public static double dividing(double a, double b) {
        double c;
        return c = a / b;
    }
}
